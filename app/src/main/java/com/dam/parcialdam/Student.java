package com.dam.parcialdam;

public class Student {
    private String name;
    private int heStudied;

    public Student() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int isHeStudied() {
        return heStudied;
    }

    public void setHeStudied(int heStudied) {
        this.heStudied = heStudied;
    }
}
